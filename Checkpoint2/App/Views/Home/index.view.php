    <h2>História</h2>
    <p>Spoločnosť Xiaomi bola založená siedmymi partnermi 6.apríla 2010. Do spoločnosti Xiaomi investovala investičná
        spoločnosť Temasek, sigapurská vláda, čínske fondy rizikových kapitálov IDG Capital a Qiming Venture Partners,
        taktiež spoločnosť, ktorá sa zaoberá výrobou mobilných čipov Qualcomm.</p>
    <img class="img-fluid" src="https://xiaomiplanet.sk/wp-content/uploads/2020/04/xiaomi-medzinarodny-prezident-show-zi.jpg" alt="majitel">
    <p>CEO spoločnosti Lei Jun si dôkladne zostavil svoj tím, vrátane bývalých zamestnancov Microsoftu, Motoroly a 16.
