    <h1>O spoločnosti</h1>

    <p> Xiaomi(čínskymi znakmi 小米) je čínska spoločnosť, ktorá navrhuje, vyvíja a predáva smartfóny, aplikácie a
        spotrebnú elektroniku. Od vydania
        svojho prvého smarfónu v auguste 2011 získali podiel na trhu v Číne a rozšírili sortiment o širšie spektrum
        spotrebnej elektroniky. Cieľom Xiaomi je udržať vysokú kvalitu a nízke ceny. Dnes má spoločnosť viac ako tri
        tisícky zamestnancov a jej hodnota sa odhaduje na 10 miliárd dolárov, čo ju stavia na rovnakú úroveň ako
        Lenovo, druhý najväčší výrobca počítačov na svete.</p>

    <p>V období Q3 2014 sa spoločnosť dostala na pozíciu svetovej
        trojky v počte vyrobených smartfónov (za Samsung a Apple) s rastom 246,2%. Koncom roka 2019 spoločnosť Xiaomi
        bola 4. najväčším predajcom smartfónov na svete so 124-miliónmi predaných smartfónov po Samsungu, Huawei a
        Apple.</p>

